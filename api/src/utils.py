import re



def get_field_type(string: str):
    patterns = {
        "date": r'^((0?[13578]|10|12)(-|\/)(([1-9])|(0[1-9])|([12])([0-9]?)|(3[01]?))(-|\/)((19)([2-9])(\d{1})|(20)(['
                r'01])(\d{1})|([8901])(\d{1}))|(0?[2469]|11)(-|\/)(([1-9])|(0[1-9])|([12])([0-9]?)|(3[0]?))(-|\/)(('
                r'19)([2-9])(\d{1})|(20)([01])(\d{1})|([8901])(\d{1})))$ ',
        "phone": r'^\+?\s?[7,8]\s?-?\(?\d\d\d\)?\s?\d\d\d\s?-?\d\d\s?-?\d\d',
        "email": r'^[a-zA-Z0-9.!#$%&\'*+/=?^_`{|}~-]+@[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?(?:\.[a-zA-Z0-9]('
                 r'?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?)*$ ',
    }

    for pattern in patterns:
        if re.match(patterns[pattern], string):
            return pattern
    return 'text'


